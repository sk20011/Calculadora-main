class Calculadora {
    // Método para sumar dos números
    sumar(num1, num2) {
        return num1 + num2;
    }

    // Método para restar dos números
    restar(num1, num2) {
        return num1 - num2;
    }

    // Método para multiplicar dos números
    multiplicar(num1, num2) { 
        return num1 * num2;
    }

    // Método para dividir dos números
    dividir(num1, num2) {
        return num1 / num2;
    }
}
